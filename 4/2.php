$GLOBALS['path'] = 'https://www.php.net/images/logos/';

function renderImg($filename)
{
    global $path;

    $url = $path . $filename;

    $img = '<img';
    $img .= " src=\"$url\"";

    $img .= '/>';

    return $img;
}

echo renderImg('php-logo.svg');